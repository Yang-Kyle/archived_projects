#include<stdio.h>
int main()
{
int shuzi,weishu,nxdy,ge,shi,hun,thou,wan; 
printf("请您输入一个不多于五位的正整数\n");
scanf("%d",&shuzi);
shuzi=(int)shuzi;
if(shuzi>99999||shuzi<=0)
{
printf("您输入的数字不是一个不多于五位的正整数\n");
}
else
{ 
printf("您输入的整数为%d\n",shuzi);
wan=(int)(shuzi/10000);
if(wan==0)
{
weishu=4;
}
else
{
weishu=5;
}
thou=(shuzi-wan*10000)/1000;
if(thou==0)
{
weishu=3;
}
hun=(shuzi-wan*10000-thou*1000)/100;
if(hun==0)
{
weishu=2;
}
shi=(shuzi-wan*10000-thou*1000-hun*100)/10;
if(shi==0)
{
weishu=1;
}
ge=shuzi-wan*10000-thou*1000-hun*100-shi*10;

printf("您输入的整数为%d位数\n",weishu);
if(weishu==5)
{ 
printf("其万位数为%d\n",wan);
} 
if(weishu>=4)
{ 
printf("其千位数为%d\n",thou);
}
if(weishu>=3)
{
printf("其百位数为%d\n",hun);
}
if(weishu>=2)
{
printf("其十位数为%d\n",shi);
}
if(weishu>=1)
{
printf("其个位数为%d\n",ge);
}
if(weishu==5)
{ 
ge*=10000;
shi*=1000;
hun*=100;
thou*=10;
nxdy=ge+shi+hun+thou+wan;
}
if(weishu==4)
{
ge*=1000;
shi*=100;
hun*=10;
nxdy=ge+shi+hun+thou;
}
if(weishu==3)
{
ge*=100;
shi*=10;
nxdy=ge+shi+hun;
}
if(weishu==2)
{
ge*=10;
nxdy=ge+shi;
}
if(weishu==1)
{
nxdy=ge;
}
printf("逆序打印输出的新数字为%d\n",nxdy);
} 
return 0;
}
